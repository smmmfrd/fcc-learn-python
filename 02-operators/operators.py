print("***** WEIRD OPERATORS *****")

# Prints 5.0 because division creates a float.
print("25 / 5" + "\t= " + str(25 / 5) + "\t(raw division)")

print("24 / 5" + "\t= " + str(round(24 / 5)) + "\t(rounded)")

print("24 / 5" + "\t= " + str(24 % 5) + "\t(remainder)")

print("2 ^ 3" + "\t= " + str(2**3) + "\t(power of)")

print("***** BOOLEAN OPERATORS *****")

print("4.0 == 4" + "\t= " + str(4.0 == 4))

print("not True" + "\t= " + str(not True))

print("True and False" + "\t= " + str(True and False))

print("True or False" + "\t= " + str(True or False))
